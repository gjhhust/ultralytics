class BBoxFeatureExtractor(nn.Module):
    def __init__(self, feature_channels):
        """
        初始化模型。
        输入:
            feature_channels: 每个尺度特征图的通道数 [C0, C1, C2, C3]
        """
        super(BBoxFeatureExtractor, self).__init__()
        self.feature_channels = feature_channels

        # 用于将融合后的特征压缩为实例特征
        self.conv = Conv(sum(feature_channels), 2 * max(feature_channels), k=1)
        
        # 计算对比学习损失
        self.criterion = ContrastiveLoss()

    def forward(self, features, bboxes, gt_ids):
        """
        输入:
            features: 多尺度特征图
            bboxes: 边界框 [B, 4]，形式为 [cx, cy, w, h]
            gt_ids: gt_ids: 实例的 gt_id [B]
        输出:
            instance_features: 实例特征 [B, 2*C, 1, 1]
        """
        # 提取每个 bbox 对应的特征并对齐
        aligned_features = []
        for i, (feature, bbox) in enumerate(zip(features, bboxes)):
            # 提取 bbox 区域的特征
            bbox_feature = self.extract_bbox_feature(feature, bbox)

            # 将低分辨率特征对齐到最高分辨率
            if i > 0:
                bbox_feature = F.interpolate(bbox_feature, size=(features[0].size(2), features[0].size(3)), mode='bilinear', align_corners=False)

            aligned_features.append(bbox_feature)

        # 按照通道维度拼接特征
        fused_feature = torch.cat(aligned_features, dim=1)  # [B, C0+C1+C2+C3, W0, H0]

        # 压缩为实例特征
        instance_features = self.conv(fused_feature)  # [B, 2*C, 1, 1]

        loss = self.criterion(instance_features, gt_ids)
        return loss

    def extract_bbox_feature(self, feature, bbox):
        """
        从特征图中提取 bbox 对应的区域。
        输入:
            feature: 特征图 [B, C, H, W]
            bbox: 边界框 [4]，形式为 [cx, cy, w, h]
        输出:
            bbox_feature: bbox 区域的特征 [B, C, W0, H0]
        """
        B, C, H, W = feature.shape
        cx, cy, w, h = bbox

        # 计算 bbox 的左上角和右下角坐标
        x1 = int((cx - w / 2) * W)
        y1 = int((cy - h / 2) * H)
        x2 = int((cx + w / 2) * W)
        y2 = int((cy + h / 2) * H)

        # 提取 bbox 区域的特征
        bbox_feature = feature[:, :, y1:y2, x1:x2]

        return bbox_feature


class ContrastiveLoss(nn.Module):
    def __init__(self, temperature=0.1):
        """
        初始化对比学习损失。
        输入:
            temperature: 温度参数，控制对比学习的难度
        """
        super(ContrastiveLoss, self).__init__()
        self.temperature = temperature

    def forward(self, instance_features, gt_ids):
        """
        输入:
            instance_features: 实例特征 [B, 2*C, 1, 1]
            gt_ids: 实例的 gt_id [B]
        输出:
            loss: 对比学习损失
        """
        B = instance_features.size(0)

        # 将实例特征展平为 [B, 2*C]
        instance_features = instance_features.view(B, -1)

        # 计算相似度矩阵
        similarity_matrix = torch.matmul(instance_features, instance_features.T) / self.temperature  # [B, B]

        # 计算对比学习损失
        loss = 0
        for i in range(B):
            pos_mask = gt_ids == gt_ids[i]  # 正样本掩码
            neg_mask = ~pos_mask  # 负样本掩码

            # 正样本相似度
            pos_sim = similarity_matrix[i, pos_mask]
            # 负样本相似度
            neg_sim = similarity_matrix[i, neg_mask]

            # 计算 InfoNCE 损失
            numerator = torch.exp(pos_sim).sum()
            denominator = torch.exp(neg_sim).sum() + numerator
            loss += -torch.log(numerator / denominator)

        return loss / B


def sigmoid(x, k):
    return 1/(1+torch.exp(-(1/k) * x))

def xyxy2xyw2h2(bbox):
    _bbox = bbox.clone()
    _bbox[:,0] = bbox[:,0]
    _bbox[:,1] = bbox[:,1]
    _bbox[:,2] = (bbox[:,2] - bbox[:,0])/2
    _bbox[:,3] = (bbox[:,3] - bbox[:,1])/2
    return _bbox

def fp16_clamp(x, min=None, max=None):
    if not x.is_cuda and x.dtype == torch.float16:
        # clamp for cpu float16, tensor fp16 has no clamp implementation
        return x.float().clamp(min, max).half()

    return x.clamp(min, max)

def bbox_overlaps(bboxes1, bboxes2, mode='iou', is_aligned=False, eps=1e-6):
    """Calculate overlap between two set of bboxes.

    FP16 Contributed by https://github.com/open-mmlab/mmdetection/pull/4889
    Note:
        Assume bboxes1 is M x 4, bboxes2 is N x 4, when mode is 'iou',
        there are some new generated variable when calculating IOU
        using bbox_overlaps function:

        1) is_aligned is False
            area1: M x 1
            area2: N x 1
            lt: M x N x 2
            rb: M x N x 2
            wh: M x N x 2
            overlap: M x N x 1
            union: M x N x 1
            ious: M x N x 1

            Total memory:
                S = (9 x N x M + N + M) * 4 Byte,

            When using FP16, we can reduce:
                R = (9 x N x M + N + M) * 4 / 2 Byte
                R large than (N + M) * 4 * 2 is always true when N and M >= 1.
                Obviously, N + M <= N * M < 3 * N * M, when N >=2 and M >=2,
                           N + 1 < 3 * N, when N or M is 1.

            Given M = 40 (ground truth), N = 400000 (three anchor boxes
            in per grid, FPN, R-CNNs),
                R = 275 MB (one times)

            A special case (dense detection), M = 512 (ground truth),
                R = 3516 MB = 3.43 GB

            When the batch size is B, reduce:
                B x R

            Therefore, CUDA memory runs out frequently.

            Experiments on GeForce RTX 2080Ti (11019 MiB):

            |   dtype   |   M   |   N   |   Use    |   Real   |   Ideal   |
            |:----:|:----:|:----:|:----:|:----:|:----:|
            |   FP32   |   512 | 400000 | 8020 MiB |   --   |   --   |
            |   FP16   |   512 | 400000 |   4504 MiB | 3516 MiB | 3516 MiB |
            |   FP32   |   40 | 400000 |   1540 MiB |   --   |   --   |
            |   FP16   |   40 | 400000 |   1264 MiB |   276MiB   | 275 MiB |

        2) is_aligned is True
            area1: N x 1
            area2: N x 1
            lt: N x 2
            rb: N x 2
            wh: N x 2
            overlap: N x 1
            union: N x 1
            ious: N x 1

            Total memory:
                S = 11 x N * 4 Byte

            When using FP16, we can reduce:
                R = 11 x N * 4 / 2 Byte

        So do the 'giou' (large than 'iou').

        Time-wise, FP16 is generally faster than FP32.

        When gpu_assign_thr is not -1, it takes more time on cpu
        but not reduce memory.
        There, we can reduce half the memory and keep the speed.

    If ``is_aligned`` is ``False``, then calculate the overlaps between each
    bbox of bboxes1 and bboxes2, otherwise the overlaps between each aligned
    pair of bboxes1 and bboxes2.

    Args:
        bboxes1 (Tensor): shape (B, m, 4) in <x1, y1, x2, y2> format or empty.
        bboxes2 (Tensor): shape (B, n, 4) in <x1, y1, x2, y2> format or empty.
            B indicates the batch dim, in shape (B1, B2, ..., Bn).
            If ``is_aligned`` is ``True``, then m and n must be equal.
        mode (str): "iou" (intersection over union), "iof" (intersection over
            foreground) or "giou" (generalized intersection over union).
            Default "iou".
        is_aligned (bool, optional): If True, then m and n must be equal.
            Default False.
        eps (float, optional): A value added to the denominator for numerical
            stability. Default 1e-6.

    Returns:
        Tensor: shape (m, n) if ``is_aligned`` is False else shape (m,)

    Example:
        >>> bboxes1 = torch.FloatTensor([
        >>>     [0, 0, 10, 10],
        >>>     [10, 10, 20, 20],
        >>>     [32, 32, 38, 42],
        >>> ])
        >>> bboxes2 = torch.FloatTensor([
        >>>     [0, 0, 10, 20],
        >>>     [0, 10, 10, 19],
        >>>     [10, 10, 20, 20],
        >>> ])
        >>> overlaps = bbox_overlaps(bboxes1, bboxes2)
        >>> assert overlaps.shape == (3, 3)
        >>> overlaps = bbox_overlaps(bboxes1, bboxes2, is_aligned=True)
        >>> assert overlaps.shape == (3, )

    Example:
        >>> empty = torch.empty(0, 4)
        >>> nonempty = torch.FloatTensor([[0, 0, 10, 9]])
        >>> assert tuple(bbox_overlaps(empty, nonempty).shape) == (0, 1)
        >>> assert tuple(bbox_overlaps(nonempty, empty).shape) == (1, 0)
        >>> assert tuple(bbox_overlaps(empty, empty).shape) == (0, 0)
    """

    assert mode in ['iou', 'iof', 'giou'], f'Unsupported mode {mode}'
    # Either the boxes are empty or the length of boxes' last dimension is 4
    assert (bboxes1.size(-1) == 4 or bboxes1.size(0) == 0)
    assert (bboxes2.size(-1) == 4 or bboxes2.size(0) == 0)

    # Batch dim must be the same
    # Batch dim: (B1, B2, ... Bn)
    assert bboxes1.shape[:-2] == bboxes2.shape[:-2]
    batch_shape = bboxes1.shape[:-2]

    rows = bboxes1.size(-2)
    cols = bboxes2.size(-2)
    if is_aligned:
        assert rows == cols

    if rows * cols == 0:
        if is_aligned:
            return bboxes1.new(batch_shape + (rows, ))
        else:
            return bboxes1.new(batch_shape + (rows, cols))

    area1 = (bboxes1[..., 2] - bboxes1[..., 0]) * (
        bboxes1[..., 3] - bboxes1[..., 1])
    area2 = (bboxes2[..., 2] - bboxes2[..., 0]) * (
        bboxes2[..., 3] - bboxes2[..., 1])

    if is_aligned:
        lt = torch.max(bboxes1[..., :2], bboxes2[..., :2])  # [B, rows, 2]
        rb = torch.min(bboxes1[..., 2:], bboxes2[..., 2:])  # [B, rows, 2]

        wh = fp16_clamp(rb - lt, min=0)
        overlap = wh[..., 0] * wh[..., 1]

        if mode in ['iou', 'giou']:
            union = area1 + area2 - overlap
        else:
            union = area1
        if mode == 'giou':
            enclosed_lt = torch.min(bboxes1[..., :2], bboxes2[..., :2])
            enclosed_rb = torch.max(bboxes1[..., 2:], bboxes2[..., 2:])
    else:
        lt = torch.max(bboxes1[..., :, None, :2],
                       bboxes2[..., None, :, :2])  # [B, rows, cols, 2]
        rb = torch.min(bboxes1[..., :, None, 2:],
                       bboxes2[..., None, :, 2:])  # [B, rows, cols, 2]

        wh = fp16_clamp(rb - lt, min=0)
        overlap = wh[..., 0] * wh[..., 1]

        if mode in ['iou', 'giou']:
            union = area1[..., None] + area2[..., None, :] - overlap
        else:
            union = area1[..., None]
        if mode == 'giou':
            enclosed_lt = torch.min(bboxes1[..., :, None, :2],
                                    bboxes2[..., None, :, :2])
            enclosed_rb = torch.max(bboxes1[..., :, None, 2:],
                                    bboxes2[..., None, :, 2:])

    eps = union.new_tensor([eps])
    union = torch.max(union, eps)
    ious = overlap / union
    if mode in ['iou', 'iof']:
        return ious
    # calculate gious
    enclose_wh = fp16_clamp(enclosed_rb - enclosed_lt, min=0)
    enclose_area = enclose_wh[..., 0] * enclose_wh[..., 1]
    enclose_area = torch.max(enclose_area, eps)
    gious = ious - (enclose_area - union) / enclose_area
    return gious

def safit_loss(pred, target, eps=1e-7, C=32,
               reduction=None, avg_factor=None):
    r"""`Implementation of Distance-IoU Loss: Faster and Better
    Learning for Bounding Box Regression, https://arxiv.org/abs/1911.08287`_.

    Code is modified from https://github.com/Zzh-tju/DIoU.

    Args:
        pred (Tensor): Predicted bboxes of format (x1, y1, x2, y2),
            shape (n, 4).
        target (Tensor): Corresponding gt bboxes, shape (n, 4).
        eps (float): Eps to avoid log(0).
    Return:
        Tensor: Loss tensor.
    """
    # IOU
    ious = bbox_overlaps(pred, target, is_aligned=True).clamp(min=eps)
    
    # NWD
    pred_xyhw = xyxy2xyw2h2(pred)
    target_xyhw = xyxy2xyw2h2(target)
    nwd = torch.exp(-torch.sqrt(torch.sum((pred_xyhw - target_xyhw)*(pred_xyhw - target_xyhw),dim=1))/C)
 
    # SAFit
    ag = (target[:, 2] - target[:, 0]) * (target[:, 3] - target[:, 1])
    area = ag
    safit = sigmoid(torch.sqrt(area)-C, C) * ious+\
            (1-sigmoid(torch.sqrt(area)-C, C)) * nwd
    loss = 1 - safit
    return loss

def nwd_loss(pred, target, eps=1e-7, C=32,
             reduction=None, avg_factor=None):
    r"""`Implementation of Distance-IoU Loss: Faster and Better
    Learning for Bounding Box Regression, https://arxiv.org/abs/1911.08287`_.

    Code is modified from https://github.com/Zzh-tju/DIoU.

    Args:
        pred (Tensor): Predicted bboxes of format (x1, y1, x2, y2),
            shape (n, 4).
        target (Tensor): Corresponding gt bboxes, shape (n, 4).
        eps (float): Eps to avoid log(0).
    Return:
        Tensor: Loss tensor.
    """
    pred, target = xyxy2xyw2h2(pred), xyxy2xyw2h2(target)
    nwd = torch.exp(-torch.norm((pred-target), p=2, dim=1)/C)
    loss = 1 - nwd.clamp(min=-1.0, max=1.0)
    return loss

class NWDLoss(nn.Module):

    def __init__(self, eps=1e-6, reduction='mean', loss_weight=1.0, C=32):
        super(NWDLoss, self).__init__()
        self.eps = eps
        self.reduction = reduction
        self.loss_weight = loss_weight
        self.C = C

    def forward(self,
                pred,
                target,
                weight=None,
                avg_factor=None,
                reduction_override=None,
                **kwargs):
        if weight is not None and not torch.any(weight > 0):
            if pred.dim() == weight.dim() + 1:
                weight = weight.unsqueeze(1)
            return (pred * weight).sum()  # 0
        assert reduction_override in (None, 'none', 'mean', 'sum')
        reduction = (
            reduction_override if reduction_override else self.reduction)
        if weight is not None and weight.dim() > 1:
            # TODO: remove this in the future
            # reduce the weight of shape (n, 4) to (n,) to match the
            # giou_loss of shape (n,)
            assert weight.shape == pred.shape
            weight = weight.mean(-1)
        loss = self.loss_weight * nwd_loss(
            pred,
            target,
            eps=self.eps,
            C = self.C,
            reduction=reduction,
            avg_factor=avg_factor,
            **kwargs)
        if reduction == "mean":
            loss = loss.mean()
        return loss
    
